const idade = 18;
const nome = 'Daniel';

if(idade == '18' && nome === 'Daniel'){
    console.log('Pode entrar')
} else {
    console.log('Não pode entrar')
}

// Observações sobre o sinal de igual ( = ) :
// = é atribuição
// == comparativo
// Ex : idade == '18'
// O conteúdo da variável idade é 18 ? Sim, então vai dar verdadeiro
// === comparativo expressivo
// Ex : idade === '18'
// O conteúdo da variável idade é uma string 18 ? Não, então vai dar falso

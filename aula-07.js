let idade = 1;

while(idade < 18){
    console.log('Passou', idade)
    idade = idade + 1;
}
// Enquanto idade for menor que 18, vai acontecer :
// 1° Exibir na tela Passou + idade, irá começar com Passou 1
// 2° Idade vai ganhar + 1
// Ou seja, vai começar com Passou 1, depois amostrar Passou 2, assim em diante até o Passou 17, pois 17 é menor que 18